package prgms.spring_week1.domain.customer.model;

public class BlackConsumer {
    private String name;
    private int age;

    public BlackConsumer(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
